#include<stdio.h>


void selectionSort(int a[],int n)
{
	int i,j,min,temp;

	for(i=0;i<n-1;i++)
	{
		min=i;
		for(j=i+1;j<n;j++)
		{
			if(a[j]<a[min])
			min=j;
		}

		if(min!=i)
		{
			temp=a[min];
			a[min]=a[i];
			a[i]=temp;
		}
	}
}

int main()
{
	int a[100],i,n;

	

	printf("Enter number of elements ");
	scanf("%d",&n);


	printf("\nEnter %d elements ",n);

	for(i=0;i<n;i++)
	scanf("%d",&a[i]);

	selectionSort(a,n);

	printf("\nSecond smallest is %d ",a[1]);
	printf("\nSecond largest is %d ",a[n-2]);


	

	return 0;
}
